
--------- ORÇAMENTO RECEITA-----------

IF OBJECT_ID ('tempdb.dbo.#TBLL1') is not null 
DROP TABLE #TBLL1

SELECT 
TIPO,
UF,
DIRETORIA,
PRODUTO,
CC,
ORCADO,
REALIZADO,
REF,
MACRO_PRODUTO,
LEFT (REF,4) AS ANO,
RIGHT (REF,2) AS MES
INTO #TBLL1
FROM
BDRV.DBO.ORCAMENTO_RECEITA_REMUNERACAO


---ORÇAMENTO

IF OBJECT_ID ('BDRV.DBO.ORCAMENTO') is not null 
DROP TABLE BDRV.DBO.ORCAMENTO

SELECT A.*,
B.MEES
INTO BDRV.DBO.ORCAMENTO
FROM #TBLL1 A
INNER JOIN
BDRV.DBO.TBL_MES B
ON A.MES = B.MES

SELECT * FROM
BDRV.DBO.ORCAMENTO
