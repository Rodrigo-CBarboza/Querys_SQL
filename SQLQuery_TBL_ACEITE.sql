
IF OBJECT_ID ('tempdb.dbo.#TBL01') is not null 
DROP TABLE #TBL01

SELECT A.REF,
A.MATRICULA,
A.CPF,
A.NOME,
A.NOVA_DIRETORIA,
A.UF,
A.PRODUTO,
A.MACRO_PRODUTO,
A.CARGO_AGRUPADO,
A.MEES,
A.ANO,
B.DATAACEITE
INTO #TBL01
FROM BDRV.DBO.RESUMO_DASHBOARD_SEREDE_CONECTA A
LEFT JOIN
BDRV.DBO.TBL_ACEITE B
ON A.REF = B.DataAceite AND
A.CPF = B.CPF

SELECT * FROM
#TBL01
WHERE
REF >= '202101'
